#include "libft.h"

int		length_int(int i)
{
	int length;

	length = 1;
	if (i < 0)
	{
		length++;
		i = i * (-1);
	}
	while (i >= 10)
	{
		i = i / 10;
		length++;
	}
	return (length);
}
