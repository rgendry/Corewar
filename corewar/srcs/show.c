#include "corewar.h"

void	show_arena(t_game *game)
{
	int i;

	i = 0;
	ft_printf("%s : ", "0x0000");
	while (i < MEM_SIZE)
	{
		ft_printf("%.2x ", game->arena[i]);
		i++;
		if (i % (2 * 32) == 0)
		{
			ft_putchar('\n');
			if (i != 256 * 16)
				ft_printf("%#.4x : ", i);
		}
	}
}
